    private void btnSparaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSparaActionPerformed
        if(val.hamtaEpost(tfEpost.getText()) == false){
            JOptionPane.showMessageDialog(this, "Email behöver vara i rätt format, xxxx.x@xxxxx.xxx");
        }else if(val.valideraNumerisk(tfTele.getText()) == false){
            JOptionPane.showMessageDialog(this, "Telefonnummret behöver vara i enbart siffror");
        }else if(val.hamtaEpost(tfEpost.getText()) == true && val.valideraNumerisk(tfTele.getText()) == true){
        try{
            String valdStad = (String) cbStad.getSelectedItem(); 
            String sqlValdStad = idb.fetchSingle("SELECT sid FROM stad WHERE namn ='" + valdStad + "'");
            String id = idb.fetchSingle("SELECT avdid FROM avdelning WHERE namn ='" + valdAvdelning + "'");
            idb.update("UPDATE avdelning SET namn ='" + tfNamn.getText() + "' WHERE avdid =" + id);
            idb.update("UPDATE avdelning SET beskrivning ='" + tfBeskrivning.getText() + "' WHERE avdid =" + id);
            idb.update("UPDATE avdelning SET adress ='" + tfAdress.getText() + "' WHERE avdid =" + id);
            idb.update("UPDATE avdelning SET epost ='" + tfEpost.getText() + "' WHERE avdid =" + id);
            idb.update("UPDATE avdelning SET telefon ='" + tfTele.getText() + "' WHERE avdid =" + id);
            idb.update("UPDATE avdelning SET stad ='" + sqlValdStad + "' WHERE avdid =" + id);
            new Avdelning(idb).setVisible(true);
            this.dispose();
        }catch(InfException ex){
            System.out.println(ex.getMessage());
        }
        }
    }//GEN-LAST:event_btnSparaActionPerformed

