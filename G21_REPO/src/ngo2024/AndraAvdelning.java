    //Skapar en ArrayList för att hämta namn på de avdlningar som finns i databasen
    public ArrayList hamtaAvdelningNamn(){
        ArrayList<String>avdelningNamnLista = new ArrayList<>(); 
        try{
            avdelningNamnLista = idb.fetchColumn("SELECT namn FROM avdelning"); 
        }catch(InfException ex){
            System.out.println(ex.getMessage());
        }
        return avdelningNamnLista; 
    }
    
    //Metod för att populera de avdelningar som finns i databasen i en combobox
    public void populeraValjAvdelning(){
        ArrayList<String>avdelningNamnLista = hamtaAvdelningNamn(); 
        for(String avdelningLista : avdelningNamnLista){
            cbValjAvdelning.addItem(avdelningLista);
        }
    }
    
    //Skapar en ArrayList för att hämta namn på de städer som finns i databasen
    public ArrayList hamtaStadNamn(){
        ArrayList<String>stadNamnLista = new ArrayList<>();
        try{
            stadNamnLista = idb.fetchColumn("SELECT namn FROM stad"); 
        }catch(InfException ex){
            System.out.println(ex.getMessage());
        }
        return stadNamnLista; 
    }
    
    //Metod för att populera de städer som finns i en combobox
    public void populeraValdStad(){
        ArrayList<String>stadNamnLista = hamtaStadNamn();
        for(String stadLista : stadNamnLista){
            cbStad.addItem(stadLista);
        }
    }
    
    //Populerar de fält som ska finns beronde på vilken avdelning man väljer
    public void populeraFalt(){
        try{
            String id = idb.fetchSingle("SELECT avdid FROM avdelning WHERE namn ='" + valdAvdelning + "'"); 
            String sqlAvdid = idb.fetchSingle("SELECT avdid FROM avdelning WHERE avdid ='" + id + "'");
            String sqlNamn = idb.fetchSingle("SELECT namn FROM avdelning WHERE avdid ='" + id + "'");
            String sqlBeskrivning = idb.fetchSingle("SELECT beskrivning FROM avdelning WHERE avdid ='" + id + "'");
            String sqlAdress = idb.fetchSingle("SELECT adress FROM avdelning WHERE avdid ='" + id + "'");
            String sqlEpost = idb.fetchSingle("SELECT epost FROM avdelning WHERE avdid ='" + id + "'");
            String sqlTele = idb.fetchSingle("SELECT telefon FROM avdelning WHERE avdid ='" + id + "'");
            tfAvdid.setText(sqlAvdid);
            tfNamn.setText(sqlNamn);
            tfBeskrivning.setText(sqlBeskrivning);
            tfAdress.setText(sqlAdress); 
            tfEpost.setText(sqlEpost);
            tfTele.setText(sqlTele);
        }catch(InfException ex){
            System.out.println(ex.getMessage()); 
        }
    }
    

