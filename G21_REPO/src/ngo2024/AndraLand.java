    //Skapar en ArrayList och spara namnen på de länder som finns i databasen
    public ArrayList hamtaLandsNamn(){
        ArrayList<String>landNamnLista = new ArrayList<>(); 
        try{
            landNamnLista = idb.fetchColumn("SELECT namn FROM land"); 
            
        }catch(InfException ex){
            System.out.println(ex.getMessage());
        }
        return landNamnLista;        
    }
    
    //Metod för att populera en combobox med namnen på de länder som finns i databasen
    public void populeraValjLand(){
        ArrayList<String>landNamnLista = hamtaLandsNamn(); 
        for(String landNamn : landNamnLista){
            cbValtLand.addItem(landNamn);
        }
    }
    
    //Metod för att populera fält som behövs med rätt information om det landet man valt. 
    public void populeraFalt(){
        try{
        String id = idb.fetchSingle("SELECT lid FROM land where namn ='" + valtLand + "'");
        String sqlLid = idb.fetchSingle("SELECT lid FROM land WHERE  lid ='" + id + "'");
        String sqlNamn = idb.fetchSingle("SELECT namn FROM land WHERE  lid ='" + id + "'");
        String sqlSprak = idb.fetchSingle("SELECT sprak FROM land WHERE  lid ='" + id + "'");
        String sqlValuta = idb.fetchSingle("SELECT valuta FROM land WHERE  lid ='" + id + "'");
        String sqlTidszon = idb.fetchSingle("SELECT tidszon FROM land WHERE  lid ='" + id + "'");
        String sqlPolitik = idb.fetchSingle("SELECT politisk_struktur FROM land WHERE  lid ='" + id + "'");
        String sqlEkonomi = idb.fetchSingle("SELECT ekonomi FROM land WHERE  lid ='" + id + "'");
        tfLid.setText(sqlLid);
        tfNamn.setText(sqlNamn); 
        tfSprak.setText(sqlSprak); 
        tfValuta.setText(sqlValuta); 
        tfTidszon.setText(sqlTidszon); 
        tfPolitik.setText(sqlPolitik); 
        tfEkonomi.setText(sqlEkonomi);
        }catch(InfException ex){
            System.out.println(ex.getMessage()); 
        }
    }
