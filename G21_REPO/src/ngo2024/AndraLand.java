    private void btnSparaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSparaActionPerformed
        if(val.valideraValuta(tfValuta.getText()) == false){
          JOptionPane.showMessageDialog(this, "Valuta behöver vara siffror samt fyra decimaler, xx.xxxx");
          this.setVisible(true);
        }else if(val.valideraValuta(tfValuta.getText()) == true){
        try{
        String id = idb.fetchSingle("SELECT lid FROM land where namn ='" + valtLand + "'");
        idb.update("UPDATE land SET namn ='" + tfNamn.getText() + "' WHERE lid =" + id);
        idb.update("UPDATE land SET sprak ='" + tfSprak.getText() + "' WHERE lid =" + id);
        idb.update("UPDATE land SET valuta =" + tfValuta.getText() + " WHERE lid =" + id);
        idb.update("UPDATE land SET tidszon ='" + tfTidszon.getText() + "' WHERE lid =" + id);
        idb.update("UPDATE land SET politisk_struktur ='" + tfPolitik.getText() + "' WHERE lid =" + id);
        idb.update("UPDATE land SET ekonomi ='" + tfEkonomi.getText() + "' WHERE lid =" + id);
        new LandsUppgifter(idb, landService).setVisible(true);
        this.dispose();
        }catch(InfException ex){
            System.out.println(ex.getMessage());
        }
        }
    }//GEN-LAST:event_btnSparaActionPerformed

