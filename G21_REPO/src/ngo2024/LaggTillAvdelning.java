    //Skapar en ArrayList för att hålla alla namn på städer som finns i databasen
     public ArrayList hamtaStadNamn(){
        ArrayList<String>stadNamnLista = new ArrayList<>();
        try{
            stadNamnLista = idb.fetchColumn("SELECT namn FROM stad"); 
        }catch(InfException ex){
            System.out.println(ex.getMessage());
        }
        return stadNamnLista; 
    }
    
     //Metod för att populera en combobox med namn på städer som finns i databsen
    public void populeraValdStad(){
        ArrayList<String>stadNamnLista = hamtaStadNamn();
        for(String stadLista : stadNamnLista){
            cbStad.addItem(stadLista);
        }
    }
    
    //Metod för att hämta nästa AvdelningsID
    public void getAvdID(){
            try{
            String nextID = idb.getAutoIncrement("avdelning", "avdid");
            tfAvdid.setText(nextID);
        }catch(InfException ex){
            System.out.println(ex.getMessage());
        }
    }

