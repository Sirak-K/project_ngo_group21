    private void btnLaggTillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLaggTillActionPerformed
        if(val.hamtaEpost(tfEpost.getText()) == false){
            JOptionPane.showMessageDialog(this, "Email behöver vara i rätt format, xxxx.x@xxxxx.xxx");
        }else if(val.valideraNumerisk(tfTele.getText()) == false){
            JOptionPane.showMessageDialog(this, "Telefonnummret behöver vara i enbart siffror");
        }else if(val.hamtaEpost(tfEpost.getText()) == true && val.valideraNumerisk(tfTele.getText()) == true){
        try{
        int dbID = Integer.parseInt(tfAvdid.getText());
        String dbNamn = tfNamn.getText(); 
        String dbBeskrivning = tfBeskrivning.getText(); 
        String dbAdress = tfAdress.getText(); 
        String dbEpost = tfEpost.getText();
        String dbTele = tfTele.getText();
        String valdStad = (String) cbStad.getSelectedItem(); 
        String dbValdStad = idb.fetchSingle("SELECT sid FROM stad WHERE namn ='" + valdStad + "'");
        String chef = null; 
        idb.insert("INSERT INTO avdelning VALUES("+ dbID +",'" + dbNamn + "','" + dbBeskrivning + "','" + dbAdress + "','" + dbEpost + "','" + dbTele + "','" + dbValdStad + "'," + chef + ")");
        new Avdelning(idb).setVisible(true);
        this.dispose();
        }catch(InfException ex){
            System.out.println(ex.getMessage());
        }
        }
    }//GEN-LAST:event_btnLaggTillActionPerformed

