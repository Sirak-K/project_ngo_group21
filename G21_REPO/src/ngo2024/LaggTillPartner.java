    private void btnLaggTillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLaggTillActionPerformed
        if(val.hamtaEpost(tfKontaktEpost.getText()) == false){
            JOptionPane.showMessageDialog(this, "Email behöver vara i rätt format: xxx.x@xxxxx.xxx");
        }else if(val.valideraInternationelltTelefonnummer(tfTele.getText()) == false){
            JOptionPane.showMessageDialog(this, "Telefonnummret behöver vara i rätt format: + följt av 10 siffror "); 
        }else if(val.hamtaEpost(tfKontaktEpost.getText()) == true && val.valideraInternationelltTelefonnummer(tfTele.getText()) == true){
            try{
            int dbID = Integer.parseInt(tfPid.getText()); 
            String dbNamn = tfNamn.getText(); 
            String dbKontaktPerson = tfKontaktPerson.getText();
            String dbKontaktEpost = tfKontaktEpost.getText(); 
            String dbTele = tfTele.getText(); 
            String dbAdress = tfAdress.getText(); 
            String dbBranch = tfBranch.getText();
            String valdStad = (String) cbStad.getSelectedItem(); 
            String dbStad = idb.fetchSingle("SELECT sid FROM stad WHERE namn ='" + valdStad + "'");
            idb.insert("INSERT INTO partner VALUES (" + dbID + ",'" + dbNamn + "','" + dbKontaktPerson + "','" + dbKontaktEpost + "','" + dbTele + "','" + dbAdress + "','" + dbBranch + "','" + dbStad + "')");
            new Partner(idb).setVisible(true);
            this.dispose();
        }catch(InfException ex){
            System.out.println(ex.getMessage());
        }
        }
    }//GEN-LAST:event_btnLaggTillActionPerformed

