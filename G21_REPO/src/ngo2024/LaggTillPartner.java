    //Metod för att hämta nästa ID i partner tabellen och lagra det i fältet tfPid. 
    public void hamtaPartnerID(){
        try{
            String nextID = idb.getAutoIncrement("partner", "pid");
            tfPid.setText(nextID);
        }catch(InfException ex){
            System.out.println(ex.getMessage());
        }
    }
    
    //Skapar en ArrayList och hämtar alla namn på städer och spar dessa. Returnera dessa i en lista.
    public ArrayList hamtaStadNamn(){
       ArrayList<String>stadNamnLista = new ArrayList<>();
       try{
           stadNamnLista = idb.fetchColumn("SELECT namn FROM stad"); 
       }catch(InfException ex){
           System.out.println(ex.getMessage());
       }
       return stadNamnLista; 
    }
    
    //Metod för att populera ComboBox för namn på städer. Använder ArrayList stadNamnLista som skapades ovan för 
    // att hjälpa till med detta.
    public void populeraStadNamn(){
        ArrayList<String>stadNamnLista = hamtaStadNamn();
        for(String stadLista : stadNamnLista){
            cbStad.addItem(stadLista);
        }
    }
    
    
    

