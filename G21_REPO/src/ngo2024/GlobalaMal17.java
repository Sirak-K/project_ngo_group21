    // En ArrayList av HashMaps med en 'for loop' som listar upp samtliga m책l i ett TextArea
    private void setUpInfo() {
        String SQLFraga = "select hid, namn, malnummer, beskrivning, prioritet from ngo_2024.hallbarhetsmal";
        try {
            ArrayList<HashMap<String, String>> lista = idb.fetchRows(SQLFraga);

            for (int i = 0; i < lista.size(); i++) {
                tfVisningsRuta.append("H책llbarhetsID: " + lista.get(i).get("hid") + "\n" + "Namn: " + lista.get(i).get("namn") + "\n" + "M책lnummer: "
                        + lista.get(i).get("malnummer") + "\n" + "Beskrivning: " + lista.get(i).get("beskrivning") + "\n" + "Prioritet: "
                        + lista.get(i).get("prioritet") + "\n" + "---------------------------------------- \n");
            }

        } catch (InfException ex) {
            ex.getMessage();

        }
    }

