    //Skapar en ArrayList för att hålla alla namn på avdelningar som finns i databasen
    public ArrayList hamtaAvdelningNamn(){
        ArrayList<String>avdelningNamnLista = new ArrayList<>();
        try{
        avdelningNamnLista = idb.fetchColumn("SELECT namn FROM avdelning"); 
        }catch(InfException ex){
            System.out.println(ex.getMessage());
        }
        return avdelningNamnLista; 
    }
    
    //Populerar en combobox med namn på alla avdelningar som finns i databasen
    public void populeraValAvdelning(){
        ArrayList<String>avdelningNamnLista = hamtaAvdelningNamn(); 
        for(String avdelningNamn : avdelningNamnLista){
            cbValAvdelning.addItem(avdelningNamn);
        }
    }
    
    //Skapar en HashMap för att hålla alla uppgifter om en viss avdelning
    public HashMap hamtaAvdelningUppgifter(String namn){
        HashMap<String, String>avdelningUppgifter = new HashMap<>(); 
        try{
            String id = idb.fetchSingle("SELECT avdid FROM avdelning WHERE namn ='" + namn + "'"); 
            avdelningUppgifter = idb.fetchRow("SELECT * FROM avdelning WHERE avdid ='" + id + "'"); 
        }catch(InfException ex){
            System.out.print(ex.getMessage()); 
        }
        return avdelningUppgifter; 
    }
    
    

