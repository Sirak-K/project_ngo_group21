    private void cbValAvdelningActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbValAvdelningActionPerformed
        txAvdelningInfo.setText("");
        var val = cbValAvdelning.getSelectedItem(); 
        if(val != null){
            val = (String) val; 
            HashMap<String, String>avdelningUppgifter = hamtaAvdelningUppgifter((String) val); 
            for(Entry<String, String> value : avdelningUppgifter.entrySet()){
                txAvdelningInfo.append(value.getKey() +": " + value.getValue() + ", \n");
            }
        }
    }//GEN-LAST:event_cbValAvdelningActionPerformed

