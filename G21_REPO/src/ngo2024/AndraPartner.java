    //Metod för att populera fälten beroende på vilken partner man väljer. 
    public void populeraFalt(){
        try{
        String id = idb.fetchSingle("SELECT pid FROM partner WHERE namn ='" + valdPartner + "'");
        String sqlPid = idb.fetchSingle("SELECT pid FROM partner WHERE pid ='" + id + "'");
        String sqlNamn = idb.fetchSingle("SELECT namn FROM partner WHERE pid ='" + id + "'");
        String sqlKontaktPerson = idb.fetchSingle("SELECT kontaktperson FROM partner WHERE pid ='" + id + "'");
        String sqlKontaktEpost = idb.fetchSingle("SELECT kontaktepost FROM partner WHERE pid ='" + id + "'");
        String sqlTele = idb.fetchSingle("SELECT telefon FROM partner WHERE pid ='" + id + "'");
        String sqlAdress = idb.fetchSingle("SELECT adress FROM partner WHERE pid ='" + id + "'");
        String sqlBranch = idb.fetchSingle("SELECT branch FROM partner WHERE pid ='" + id + "'");
        tfPid.setText(sqlPid);
        tfNamn.setText(sqlNamn);
        tfKontaktPerson.setText(sqlKontaktPerson);
        tfKontaktEpost.setText(sqlKontaktEpost);
        tfTele.setText(sqlTele);
        tfAdress.setText(sqlAdress);
        tfBranch.setText(sqlBranch);
        }catch(InfException ex){
            System.out.println(ex.getMessage());
        }
    }

