    private void btnAndraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAndraActionPerformed
        if(val.hamtaEpost(tfKontaktEpost.getText()) == false){
            JOptionPane.showMessageDialog(this, "Email behöver vara i rätt format: xxx.x@xxxxx.xxx");
        }else if(val.valideraInternationelltTelefonnummer(tfTele.getText()) == false){
            JOptionPane.showMessageDialog(this, "Telefonnummret behöver vara i rätt format: + följt av 10 siffror "); 
        }else if(val.hamtaEpost(tfKontaktEpost.getText()) == true && val.valideraInternationelltTelefonnummer(tfTele.getText()) == true){
            int taBortRuta = JOptionPane.showConfirmDialog(null, "Är du säker", "meddelande", JOptionPane.YES_NO_OPTION);
            if(taBortRuta == JOptionPane.YES_OPTION){
                try{
                String id = idb.fetchSingle("SELECT pid FROM partner WHERE namn ='" + valdPartner + "'");
                idb.update("UPDATE partner SET namn ='" + tfNamn.getText() + "' WHERE pid = " + id);
                idb.update("UPDATE partner SET kontaktperson ='" + tfKontaktPerson.getText() + "' WHERE pid = " + id);
                idb.update("UPDATE partner SET kontaktepost ='" + tfKontaktEpost.getText() + "' WHERE pid = " + id);
                idb.update("UPDATE partner SET telefon ='" + tfTele.getText() + "' WHERE pid = " + id);
                idb.update("UPDATE partner SET adress ='" + tfAdress.getText() + "' WHERE pid = " + id);
                idb.update("UPDATE partner SET branch ='" + tfBranch.getText() + "' WHERE pid = " + id);
                String valdStad = (String) cbStad.getSelectedItem();
                String dbStad = idb.fetchSingle("SELECT sid FROM stad WHERE namn ='" + valdStad + "'");
                idb.update("UPDATE partner SET stad ='" + dbStad + "' WHERE pid = " + id);
                new Partner(idb).setVisible(true);
                this.dispose();
            }catch(InfException ex){
                System.out.println(ex.getMessage());
            }
        }else{
            new AndraPartner(idb).setVisible(true);
            this.dispose();
        }
        }
    }//GEN-LAST:event_btnAndraActionPerformed

