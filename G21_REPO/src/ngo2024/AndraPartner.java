    //Skapar en ArrayList och hämtar alla partner-namn och spar dessa. >Returnera dessa i en lista. 
    public ArrayList hamtaPartnerNamn(){
        ArrayList<String>partnerNamnLista = new ArrayList<>(); 
        try{
            partnerNamnLista = idb.fetchColumn("SELECT namn FROM partner"); 
        }catch(InfException ex){
            System.out.println(ex.getMessage());
        }
        return partnerNamnLista; 
    }
    
    //Metod för att populera Combobox för alla partner-namn. Använder ArrayList partnerNamnLista som skapades ovan för
    // att hjälpa till med detta.
    public void populeraPartnerNamn(){
        ArrayList<String>partnerNamnLista = hamtaPartnerNamn();
        for(String partnerLista : partnerNamnLista){
            cbPartner.addItem(partnerLista);    
        }
    }
    
    //Skapar en ArrayList och hämtar alla namn på städer och spar dessa. Returnera dessa i en lista.
